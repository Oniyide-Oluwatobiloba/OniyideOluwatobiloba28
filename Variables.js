/*Variables(let, const and var)var can be re-assigned or updated
const cannot be changed if declared
let can be updated but not re-declared
*/

var name = "Tobiloba";
console.log(name);

//Using Let
let age =18;
console.log(age);
//Reassignment is allowed
age = 30;
console.log(age);


//Using const

const PI = 3.14;
console.log(Pi);
